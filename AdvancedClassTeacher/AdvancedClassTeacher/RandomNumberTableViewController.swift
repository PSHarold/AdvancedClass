    // MARK: - Table view data source

    override func numberOfSectionsInTableView(tableView: UITableView) -> Int {
        return 1
    }

    override func tableView(tableView: UITableView, numberOfRowsInSection section: Int) -> Int {
        if self.helper.newTest.questions.count == 1{
            return 2
        }
        else{
            return self.helper.newTest.questions.count
        }
        
    }

    
    override func tableView(tableView: UITableView, cellForRowAtIndexPath indexPath: NSIndexPath) -> UITableViewCell {
        let cell = tableView.dequeueReusableCellWithIdentifier("MyCell", forIndexPath: indexPath) 
        if indexPath.row == 0{
            cell.textLabel?.text = "关"
        }
        else{
            cell.textLabel?.text = "\(indexPath.row)道"

        }
        if indexPath.row == self.helper.newTest.randomNumber{
            cell.accessoryType = .Checkmark
        }
        return cell
    }

    override func tableView(tableView: UITableView, didSelectRowAtIndexPath indexPath: NSIndexPath) {
        self.tableView.deselectRowAtIndexPath(indexPath, animated: true)
        self.helper.newTest.randomNumber = indexPath.row
        self.navigationController?.popViewControllerAnimated(true)
    }
